function changeFont() {
  const paragraph = document.getElementById("myParagraph");
  paragraph.style.fontFamily = "Courier New, monospace";
}

function changeFontSize() {
  const paragraph = document.getElementById("myParagraph");
  paragraph.style.fontSize = "40px";
}

function changeColor() {
  const paragraph = document.getElementById("myParagraph");
  paragraph.style.color = "blue";
}
